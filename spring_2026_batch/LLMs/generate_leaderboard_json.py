import csv, json

MODEL_FILES = [
    ("Claude",   "ycbench_p26_claude_sonnet4.6_counts.csv"),
    ("GPT",      "ycbench_p26_gpt5_3_counts.csv"),
    ("Gemini",   "ycbench_p26_gemini3.1pro_counts.csv"),
    ("Grok",     "ycbench_p26_grok4_20_counts.csv"),
    ("GLM",      "ycbench_p26_glm5.1_counts.csv"),
    ("Kimi",     "ycbench_p26_kimi2.6thinking_counts.csv"),
    ("DeepSeek", "ycbench_p26_deepseekv4_counts.csv"),
    ("Qwen",     "ycbench_p26_qwen_3_6_plus_counts.csv"),
    ("MiniMax",  "ycbench_p26_minimax_M_2_7_counts.csv"),
    ("Mimo",     "ycbench_p26_xiaomi_mimo_v2_5pro_counts.csv"),
]

def load_scores(path):
    scores = {}
    with open(path) as f:
        for row in csv.DictReader(f):
            scores[row['Startup']] = int(float(row['Average (in percent)']))
    return scores

agg = []
with open('ycbench_p26_aggregate.csv') as f:
    for row in csv.DictReader(f):
        agg.append((row['Startup'], float(row['Score (x20)'])))
        if len(agg) == 20:
            break

model_scores = {name: load_scores(path) for name, path in MODEL_FILES}

result = []
for startup, _ in agg:
    model_values = {name: model_scores[name].get(startup, 0) for name, _ in MODEL_FILES}
    llm_avg = round(sum(model_values.values()) / len(model_values))
    entry = {"name": startup, "LLM Average": llm_avg, **model_values}
    result.append(entry)

output = "// Auto-generated by generate_leaderboard_json.py â€” do not edit manually\n"
output += "export type LeaderboardRow = Record<string, number | string>;\n\n"
output += "export const LEADERBOARD_DATA: LeaderboardRow[] = "
output += json.dumps(result, indent=2)
output += ";\n"

out_path = "leaderboardData.ts"
with open(out_path, "w") as f:
    f.write(output)

print(f"Written {out_path}")
